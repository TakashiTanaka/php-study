<?php

// ==========================================================================
// array_splice
// ==========================================================================

// spliceは元配列を直接変更する
// 指定した位置から指定した数の要素を削除
$scores = [30, 40, 50, 60, 70, 80];
array_splice($scores, 2, 3);

// 削除した位置に挿入
array_splice($scores, 2, 3, [100, 101]);

// 削除せずに指定した位置に挿入
array_splice($scores, 2, 0, [100, 101]);

print_r($scores);
