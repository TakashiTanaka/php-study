<?php

// ==========================================================================
// implode / explode
// ==========================================================================

//  implode
// 配列に対して、指定した区切り文字で連結して、文字型で返す
// `implode(区切り文字, 配列)`

// explode
// implodeの逆で、文字列に対して、指定した区切り文字で区切って配列に変換して、配列を返す
// `explode(区切り文字, 文字列)`

// 配列の値を文字列にて結合
$d = [2020, 11, 15];
echo implode('-', $d) . PHP_EOL;

// 文字列を配列に変換
$t = '17:32:45';
print_r(explode(':', $t));
